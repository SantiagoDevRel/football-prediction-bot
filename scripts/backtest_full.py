"""Full backtest sweep across all models, both leagues. Writes a markdown
report to docs/backtest-{date}.md.

Usage:
    python scripts/backtest_full.py
    python scripts/backtest_full.py --train-end 2025-12-31 --test-from 2026-01-01
"""
import argparse
import sys
from datetime import date, datetime
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT))

from scripts.backtest import LEAGUE_NAMES, evaluate  # noqa: E402

MODELS = ("dixon_coles", "elo", "xgboost", "stacking")


def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--train-end", default="2025-12-31")
    parser.add_argument("--test-from", default="2026-01-01")
    args = parser.parse_args()

    train_end = date.fromisoformat(args.train_end)
    test_from = date.fromisoformat(args.test_from)

    all_results: list = []
    for league_slug in LEAGUE_NAMES:
        for model_name in MODELS:
            try:
                r = evaluate(model_name, league_slug, train_end, test_from)
                all_results.append((league_slug, r))
            except Exception as exc:
                print(f"  {model_name} on {league_slug} failed: {exc}")

    # Markdown report
    out_path = ROOT / "docs" / f"backtest-{datetime.now().strftime('%Y-%m-%d')}.md"
    lines = [
        f"# Backtest report — {datetime.now().strftime('%Y-%m-%d')}",
        "",
        f"**Train cutoff:** {train_end}  ·  **Test from:** {test_from}",
        "",
        "Uniform baselines: log-loss = 1.0986, Brier = 0.6667, accuracy = 33.3%.",
        "",
    ]
    for league_slug in LEAGUE_NAMES:
        lname = LEAGUE_NAMES[league_slug]
        lines.append(f"## {lname}")
        lines.append("")
        lines.append("| Model | N | Acc | Log-loss | Brier | ROI(sim) |")
        lines.append("|---|---|---|---|---|---|")
        for s, r in all_results:
            if s != league_slug:
                continue
            lines.append(
                f"| {r.model} | {r.n} | {r.accuracy_1x2:.1%} | "
                f"{r.log_loss:.4f} | {r.brier:.4f} | {r.roi_flat_top1:+.1%} |"
            )
        lines.append("")
    lines.append("---")
    lines.append("")
    lines.append("Generated by `scripts/backtest_full.py`. See `scripts/backtest.py` for the eval harness and `docs/ROADMAP.md` for the phase plan.")

    out_path.write_text("\n".join(lines), encoding="utf-8")
    print(f"\n📄 wrote {out_path.relative_to(ROOT)}")


if __name__ == "__main__":
    main()
